tabby_cat = "\tI'm tabbled in."                           #\t tab键
persian_cat = "I'm split\non a line"                   #  \n  换行符
backslash_cat ="I'm \\ a \\ catself."                 #   \\   表示\

fat_cat ='''
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
'''

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)                    ##  """作用在于换行(上下都换行)

a = 'I am 6\'2" tall.'                    # \'  将字符串里的‘不再表示为字符串符号
